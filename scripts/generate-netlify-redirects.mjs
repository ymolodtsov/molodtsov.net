import fs from "node:fs";
import path from "node:path";

const contentRoot = path.join(process.cwd(), "content");
const staticRoot = path.join(process.cwd(), "static");
const redirectsPath = path.join(staticRoot, "_redirects");

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(full, files);
    } else if (entry.name.endsWith(".md")) {
      files.push(full);
    }
  }
  return files;
}

function frontMatter(markdown) {
  const match = markdown.match(/^---\n([\s\S]*?)\n---/);
  return match ? match[1] : "";
}

function frontMatterString(frontmatter, key) {
  const match = frontmatter.match(new RegExp(`^${key}:\\s*["']?([^"'\n]+)["']?\\s*$`, "m"));
  return match ? match[1].trim() : "";
}

function aliases(frontmatter) {
  const match = frontmatter.match(/^aliases:\n((?:\s*-\s*["']?[^"'\n]+["']?\s*\n?)+)/m);
  if (!match) {
    return [];
  }

  return match[1]
    .split("\n")
    .map((line) => line.match(/^\s*-\s*["']?([^"'\n]+)["']?\s*$/)?.[1]?.trim())
    .filter(Boolean);
}

const rules = new Map();
for (const file of walk(contentRoot)) {
  const fm = frontMatter(fs.readFileSync(file, "utf8"));
  const target = frontMatterString(fm, "url");
  if (!target) {
    continue;
  }

  for (const alias of aliases(fm)) {
    if (alias !== target) {
      rules.set(alias, target);
    }
  }
}

const output = [
  "# Generated by scripts/generate-netlify-redirects.mjs",
  ...Array.from(rules.entries())
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([from, to]) => `${from} ${to} 301`),
  "",
].join("\n");

fs.mkdirSync(staticRoot, { recursive: true });
fs.writeFileSync(redirectsPath, output);

console.log(`netlify redirects: wrote ${rules.size} rules`);
